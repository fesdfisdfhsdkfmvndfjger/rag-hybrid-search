def render_pdf_page(pdf_bytes: bytes, page_num: int, highlight_text: str = "") -> str:
    doc = fitz.open(stream=pdf_bytes, filetype="pdf")
    page_num = max(1, min(page_num, len(doc)))
    page = doc[page_num - 1]
    if highlight_text:
        for phrase in highlight_text.split("."):
            if len(phrase.strip()) > 20:
                for rect in page.search_for(phrase.strip()[:60]): 
                    page.add_highlight_annot(rect)
    img_bytes = page.get_pixmap(matrix=fitz.Matrix(2.0, 2.0), alpha=False).tobytes("png")
    doc.close()
    return base64.b64encode(img_bytes).decode()

def build_badges(result: RAGResult) -> str:
    c_color = confidence_color(result.confidence_score)
    c_pct = int(result.confidence_score * 100)
    return f'<div class="badge-row"><span class="pill" style="color:{c_color}; border-color:{c_color}40; background:{c_color}10;">✨ {confidence_label(result.confidence_score)} ({c_pct}%)</span></div>'

