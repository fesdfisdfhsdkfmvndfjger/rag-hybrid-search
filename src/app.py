def generate_markdown_report(history: list, pdf_name: str) -> str:
    md = f"# Research Report: {pdf_name}\n*Generated by Elite RAG*\n\n---\n\n"
    for item in history:
        md += f"### Q: {item['q']}\n\n**A:** {item['result'].answer}\n\n"
        if item['result'].retrieved:
            pages = sorted(list(set([r.page for r in item['result'].retrieved])))
            md += f"*Sources: Pages {pages}*\n\n---\n"
    return md

